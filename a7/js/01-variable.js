// VARIABLES

// déclaration d'une variable
// ! Les noms de variables sont sensibles à la casse et on fait la différence entre les majuscules et les minuscules 
let uneVariable;
// affetation d'une valeur à une variable
uneVariable = 'je suis ton père';
console.log(uneVariable)

// Les deux se font généralement en même temps
let hello = 12
console.log(hello)
// On peut changer la valeur d'une variable
let Hello = uneVariable
console.log(Hello)

// Les constantes 
// Le mot clé const permet de déclarer une constante immuable ( on ne peut pas modifier sa valeur)
//  const garanti que la valeur ne soit pas modifié tout au long de l'éxécution du script  
const constante = 'je suis une contante';
constante = 'autre chose'

// var permet aussi de délarer une variable 
// c'est une pratique désuète ( c'était la seule façon avant 2015)
var past = 'je suis dépassé'